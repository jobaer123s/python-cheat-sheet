"""
In this script we saw several data types and example of them.

    Integer/int(%d): 3, 123, 555 
    Float/float(%f): 2.0, 123.46
    String/str(%s): 's', 'hello world'
    Boolean/bool: True, False, 1==1, 1==2
    complex: 1+2j , 3j

    List/list: [1,2], [2, 'a', 1.2, False]
    Tuple/tuple: (1,2), (1,'one')
    Dictionary/dict: {'one': 1, 'two': 2}
    Set/set: {1, 2, 3}
    

"""


# int
type(4)
type(2344444)


# float
type(1.0)
type(22.122)


# str
type('test')
type('+')
type('hello all!')


# bool
type(True)
type(2==3)
type(2==2)


# complex
type(2j)


# list
type([2, 'two'])


# tuple
type((2,3))


# dict
type({'one':1})


# set
type({'a',1})